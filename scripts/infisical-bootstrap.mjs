#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { spawnSync } from "node:child_process";
import {
  ensureParentDir,
  filterEntriesByKeys,
  formatDotenvEntries,
  parseDotenv,
} from "./lib/dotenv-utils.mjs";
import { getInfisicalAccessToken, infisicalRequestHeaders } from "./lib/infisical-client.mjs";
import { loadInfisicalAuthEnv } from "./lib/infisical-env.mjs";
import { INFISICAL_CONFIG, syncKeysForEnv } from "./lib/secrets-config.mjs";

loadInfisicalAuthEnv();

const ROOT = process.cwd();
const DOMAIN = INFISICAL_CONFIG.domain;
const PROJECT_ID = INFISICAL_CONFIG.projectId;
const IDENTITY_NAME = process.env.INFISICAL_IDENTITY_NAME?.trim() || "oblivion-sync-bot";

async function loadEnvFile(filePath) {
  try {
    return parseDotenv(await fs.readFile(filePath, "utf8"));
  } catch (error) {
    if (error?.code === "ENOENT") {
      return [];
    }
    throw error;
  }
}

function syncEntriesFromFile(entries, envName) {
  return filterEntriesByKeys(entries, syncKeysForEnv(envName)).filter(([, value]) => value.trim() !== "");
}

async function writeFilteredEnv(outputPath, entries) {
  await ensureParentDir(outputPath);
  await fs.writeFile(outputPath, formatDotenvEntries(entries), "utf8");
}

async function apiRequest({ token, pathname, method = "GET", body }) {
  const headers = {
    "Content-Type": "application/json",
    ...infisicalRequestHeaders(DOMAIN, token),
  };

  const response = await fetch(`${DOMAIN}/api${pathname}`, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });
  const text = await response.text();
  let json = {};
  try {
    json = text ? JSON.parse(text) : {};
  } catch {
    throw new Error(`${method} ${pathname} returned non-JSON: ${text.slice(0, 200)}`);
  }
  if (!response.ok) {
    throw new Error(json.message || json.error || `${method} ${pathname} failed: ${response.status}`);
  }
  return json;
}

async function ensureMachineIdentity(adminToken) {
  const identities = await apiRequest({
    token: adminToken,
    pathname: `/v1/projects/${PROJECT_ID}/identities`,
  });
  const existing = (identities.identities || []).find((entry) => entry.name === IDENTITY_NAME);
  const identityId =
    existing?.id ||
    (
      await apiRequest({
        token: adminToken,
        pathname: `/v1/projects/${PROJECT_ID}/identities`,
        method: "POST",
        body: { name: IDENTITY_NAME },
      })
    ).identity.id;

  try {
    await apiRequest({
      token: adminToken,
      pathname: `/v1/projects/${PROJECT_ID}/memberships/identities/${identityId}`,
      method: "POST",
      body: { roles: [{ role: "admin" }] },
    });
  } catch (error) {
    const message = String(error instanceof Error ? error.message : error);
    if (!/already exists|duplicate|project scoped identity/i.test(message)) {
      throw error;
    }
  }

  let clientId;
  try {
    const created = await apiRequest({
      token: adminToken,
      pathname: `/v1/auth/universal-auth/identities/${identityId}`,
      method: "POST",
      body: {},
    });
    clientId = created.identityUniversalAuth.clientId;
  } catch (error) {
    const message = String(error instanceof Error ? error.message : error);
    if (!/already exists|duplicate|already configured/i.test(message)) {
      throw error;
    }
    const current = await apiRequest({
      token: adminToken,
      pathname: `/v1/auth/universal-auth/identities/${identityId}`,
    });
    clientId = current.identityUniversalAuth.clientId;
  }

  const secret = await apiRequest({
    token: adminToken,
    pathname: `/v1/auth/universal-auth/identities/${identityId}/client-secrets`,
    method: "POST",
    body: { description: "oblivion-bootstrap" },
  });

  return {
    clientId,
    clientSecret: secret.clientSecret,
  };
}

async function writeInfisicalEnv({ clientId, clientSecret }) {
  const lines = [
    "# Generated by scripts/infisical-bootstrap.mjs",
    `INFISICAL_MACHINE_CLIENT_ID=${clientId}`,
    `INFISICAL_MACHINE_CLIENT_SECRET=${clientSecret}`,
    `INFISICAL_PROJECT_ID=${PROJECT_ID}`,
    `INFISICAL_ORGANIZATION_ID=${INFISICAL_CONFIG.organizationId}`,
    `INFISICAL_DOMAIN=${DOMAIN}`,
  ];
  if (process.env.CF_ACCESS_CLIENT_ID && process.env.CF_ACCESS_CLIENT_SECRET) {
    lines.push(`CF_ACCESS_CLIENT_ID=${process.env.CF_ACCESS_CLIENT_ID}`);
    lines.push(`CF_ACCESS_CLIENT_SECRET=${process.env.CF_ACCESS_CLIENT_SECRET}`);
  }
  await fs.writeFile(path.resolve(ROOT, ".env.infisical"), `${lines.join("\n")}\n`, "utf8");
}

function runSync(command, envName, extraArgs = []) {
  const result = spawnSync(
    process.execPath,
    ["scripts/infisical-sync.mjs", command, "--env", envName, ...extraArgs],
    {
      cwd: ROOT,
      encoding: "utf8",
      env: process.env,
    },
  );

  if (result.status !== 0) {
    throw new Error(
      result.stderr.trim() || result.stdout.trim() || `infisical-sync ${command} ${envName} failed`,
    );
  }

  process.stdout.write(result.stdout);
}

async function main() {
  const token = await getInfisicalAccessToken(DOMAIN);
  const devEntries = syncEntriesFromFile(await loadEnvFile(path.resolve(ROOT, ".env")), "dev");
  const prodSource = await loadEnvFile(path.resolve(ROOT, ".env.production"));
  const prodEntries = syncEntriesFromFile(
    prodSource.length > 0 ? prodSource : await loadEnvFile(path.resolve(ROOT, ".env")),
    "prod",
  );

  const devPushPath = path.resolve(ROOT, "tmp/secrets/oblivion.dev.push.env");
  const prodPushPath = path.resolve(ROOT, "tmp/secrets/oblivion.prod.push.env");

  await writeFilteredEnv(devPushPath, devEntries);
  await writeFilteredEnv(prodPushPath, prodEntries);

  console.log(`Pruning legacy keys from Infisical...`);
  runSync("prune", "dev");
  runSync("prune", "prod");

  console.log(`Pushing ${devEntries.length} dev secrets...`);
  runSync("push", "dev", ["--input", devPushPath]);

  console.log(`Pushing ${prodEntries.length} prod secrets...`);
  runSync("push", "prod", ["--input", prodPushPath]);

  runSync("pull", "dev");
  runSync("pull", "prod");

  let identity;
  try {
    identity = await ensureMachineIdentity(token);
    await writeInfisicalEnv(identity);
    console.log(`Wrote .env.infisical for machine identity ${IDENTITY_NAME}`);
  } catch (error) {
    console.warn(
      `Could not create dedicated machine identity (${error instanceof Error ? error.message : error}).`,
    );
    console.warn("Falling back to existing universal-auth credentials in the shell.");
    if (process.env.INFISICAL_MACHINE_CLIENT_ID && process.env.INFISICAL_MACHINE_CLIENT_SECRET) {
      await writeInfisicalEnv({
        clientId: process.env.INFISICAL_MACHINE_CLIENT_ID,
        clientSecret: process.env.INFISICAL_MACHINE_CLIENT_SECRET,
      });
    }
  }

  console.log("Infisical bootstrap complete.");
  console.log("Next: npm run secrets:doctor");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});