#!/usr/bin/env node
/**
 * Provision dedicated Forgejo service users for Oblivion Party Quest agents.
 *
 * Environment:
 *   FORGEJO_TOKEN        — Forgejo admin token (required)
 *   FORGEJO_BASE_URL     — default https://forgejo.phantasy.bot
 *   WORKSPACE_ENV_DIR    — default ~/oblivion-ops/workspaces/env
 *   ROTATE_TOKENS        — set to 1 to recreate access tokens for existing users
 */

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { homedir } from 'node:os';
import path from 'node:path';

const forgejoBase = (process.env.FORGEJO_BASE_URL || 'https://forgejo.phantasy.bot').replace(
  /\/$/,
  '',
);
const forgejoToken = process.env.FORGEJO_TOKEN?.trim();
const workspaceEnvDir = (
  process.env.WORKSPACE_ENV_DIR || path.join(homedir(), 'oblivion-ops', 'workspaces', 'env')
).replace(/^~(?=$|[\\/])/, homedir());
const rotateTokens = process.env.ROTATE_TOKENS === '1';

const AGENT_MANIFEST = [
  {
    agentFrameworkId: 'oblivion-phantasy-agent',
    agentId: 'oblivion-marketing',
    username: 'oblivion-phantasy',
    fullName: 'Oblivion Phantasy Agent',
    email: 'oblivion-phantasy@phantasy.bot',
    workspace: 'phantasy',
  },
  {
    agentFrameworkId: 'oblivion-hermes-agent',
    agentId: 'oblivion-research',
    username: 'oblivion-hermes',
    fullName: 'Oblivion Hermes Agent',
    email: 'oblivion-hermes@phantasy.bot',
    workspace: 'hermes',
  },
  {
    agentFrameworkId: 'oblivion-openclaw-agent',
    agentId: 'oblivion-debug',
    username: 'oblivion-openclaw',
    fullName: 'Oblivion OpenClaw Agent',
    email: 'oblivion-openclaw@phantasy.bot',
    workspace: 'openclaw',
  },
  {
    agentFrameworkId: 'oblivion-opencode-agent',
    agentId: 'oblivion-code',
    username: 'oblivion-opencode',
    fullName: 'Oblivion OpenCode Agent',
    email: 'oblivion-opencode@phantasy.bot',
    workspace: 'opencode',
  },
];

async function forgejoApi(apiPath, init = {}) {
  const response = await fetch(`${forgejoBase}/api/v1${apiPath}`, {
    ...init,
    headers: {
      Accept: 'application/json',
      Authorization: `token ${forgejoToken}`,
      'Content-Type': 'application/json',
      ...(init.headers || {}),
    },
  });
  const text = await response.text();
  const body = text ? JSON.parse(text) : null;
  if (!response.ok) {
    throw new Error(`Forgejo ${init.method || 'GET'} ${apiPath} failed: ${JSON.stringify(body)}`);
  }
  return body;
}

async function ensureUser(entry) {
  try {
    const existing = await forgejoApi(`/users/${encodeURIComponent(entry.username)}`);
    return { user: existing, created: false };
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    if (!message.includes('404') && !message.includes('does not exist')) {
      throw error;
    }
  }

  const created = await forgejoApi('/admin/users', {
    method: 'POST',
    body: JSON.stringify({
      username: entry.username,
      full_name: entry.fullName,
      email: entry.email,
      password: crypto.randomUUID(),
      must_change_password: false,
      send_notify: false,
    }),
  });
  return { user: created, created: true };
}

async function ensureAccessToken(entry, userId) {
  const envPath = path.join(workspaceEnvDir, `${entry.agentId}.env`);
  if (!rotateTokens) {
    try {
      const existing = await readFile(envPath, 'utf8');
      if (/^FORGEJO_TOKEN=/m.test(existing)) {
        return { token: null, reused: true, envPath };
      }
    } catch {
      // no existing env file
    }
  }

  const tokenName = `party-quest-${entry.agentFrameworkId}`;
  const token = await forgejoApi(`/admin/users/${userId}/tokens`, {
    method: 'POST',
    body: JSON.stringify({ name: tokenName, scopes: ['all'] }),
  });
  return { token: token.sha1, reused: false, envPath };
}

async function writeAgentEnv(entry, accessToken) {
  const envPath = path.join(workspaceEnvDir, `${entry.agentId}.env`);
  const lines = [
    `# ${entry.fullName} — generated by setup-forgejo-agent-users.mjs`,
    `FORGEJO_BASE_URL=${forgejoBase}`,
    `FORGEJO_TOKEN=${accessToken}`,
    `FORGEJO_REPO_OWNER=oblivion`,
    `FORGEJO_REPO_NAME=oblivion`,
    `FORGEJO_DEFAULT_BRANCH=main`,
    `PARTY_QUEST_AGENT_FRAMEWORK_ID=${entry.agentFrameworkId}`,
    `FORGEJO_USERNAME=${entry.username}`,
    '',
  ];
  await mkdir(workspaceEnvDir, { recursive: true });
  await writeFile(envPath, lines.join('\n'), { mode: 0o600 });
  return envPath;
}

async function main() {
  if (!forgejoToken) {
    throw new Error('FORGEJO_TOKEN is required');
  }

  const results = [];
  for (const entry of AGENT_MANIFEST) {
    const { user, created } = await ensureUser(entry);
    const tokenResult = await ensureAccessToken(entry, user.id);
    let envPath = tokenResult.envPath;
    if (tokenResult.token) {
      envPath = await writeAgentEnv(entry, tokenResult.token);
    }
    results.push({
      agentId: entry.agentId,
      agentFrameworkId: entry.agentFrameworkId,
      username: entry.username,
      userId: user.id,
      userCreated: created,
      tokenReused: tokenResult.reused,
      envPath,
    });
  }

  console.log(JSON.stringify({ ok: true, workspaceEnvDir, agents: results }, null, 2));
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});